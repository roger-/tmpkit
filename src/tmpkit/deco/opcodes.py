# Centralized TMP opcode tables.
#
# Notes:
# - Different TP-Link firmware/app lines use different opcode ranges.
# - Some opcode IDs intentionally have multiple names (aliases) in upstream sources.
#
# Sources:
# - https://github.com/ropbear/tmpcli/issues/2 (Tether app v4.10.4, classes5\xs\i0.kt)
# - Local reversing of Deco Android app (com.tplink.tpm5 v1.10.5)


def build_opcode_alias_map(
    pairs: Iterable[tuple[int, str]],
) -> dict[int, tuple[str, ...]]:
    """Build a map of opcode -> names, preserving duplicates as aliases."""

    tmp: dict[int, list[str]] = {}
    for code, name in pairs:
        tmp.setdefault(int(code), []).append(str(name))
    return {code: tuple(names) for code, names in tmp.items()}


def opcode_names(table: dict[int, tuple[str, ...]], code: int) -> tuple[str, ...]:
    """Return known names for an opcode ID (empty tuple if unknown)."""

    return table.get(int(code), ())


